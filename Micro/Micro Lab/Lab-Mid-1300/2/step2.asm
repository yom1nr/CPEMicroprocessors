	ORG	0000
	
	MOV	DPTR,#8000H
	
LOOP_:	MOV	A,#11H
	MOVX	@DPTR,A
	INC	DPTR
	
	MOV	R0,DPL
	CJNE	R0,#0FFH,LOOP_
	MOV	R0,DPH
	CJNE	R0,#080H,LOOP_
	
