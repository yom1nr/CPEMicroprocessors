ORG 	0000H
	JMP 0100H
	ORG 0100H
	MOV SP,#2FH
	
LOOP: 	CALL LED_R2L
	JMP LOOP
	
LED_R2L:
	MOV A,#00000001B
	CALL Delay
	MOV A,#00000010B
	CALL Delay
	MOV A,#00000100B
	CALL Delay
	MOV A,#00001000B
	CALL Delay
	MOV A,#00010000B
	CALL Delay
	MOV A,#00100000B
	CALL Delay
	MOV A,#01000000B
	CALL Delay
	MOV A,#10000000B
	CALL Delay
	RET
	
Delay: 	CPL A
	MOV P2,A
	MOV R0,#5
	MOV B,#00H
	MOV A,#00H
	
_DLY00: DJNZ Acc,_DLY00
	DJNZ B,_DLY00
	DJNZ R0,_DLY00
	RET
	END